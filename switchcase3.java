public class switchcase3{  
    public static void main(String[] args) {   
        int number=20;  
 switch(number){  
   case 10: System.out.println("30");  
        break;  
        case 20: System.out.println("1");  
        break;  
        case 30: System.out.println("5");  
        break;  
        default:System.out.println("Not in 30, 1or 5");  
        }  
    }  
}
